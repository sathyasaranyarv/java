class Variable
{
public static void main(String args[])
{
char number=5;
switch(number)
{
case 3:
System.out.println("smallest number");
break;
case 4:
System.out.println("second largest number");
break;
case 5:
System.out.println("largest number");
break;
default:
System.out.println("invalid number");
}
}
}
